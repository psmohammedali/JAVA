package com.company;

public class whileLoop {
    public static void main(String[] args) {
        int x =10;
        while(x<15){
            System.out.println(x);
            ++x;
            System.out.println("====");
            System.out.println(x);
            System.out.println("====");
        }
        System.out.println(x);
        System.out.println(x);
    }
}


